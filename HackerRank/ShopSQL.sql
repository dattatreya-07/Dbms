-- ── CUSTOMERS ────────────────────────────────────────────────
CREATE TABLE CUSTOMERS (
    CUSTOMER_ID   NUMBER        PRIMARY KEY,
    FULL_NAME     VARCHAR2(100) NOT NULL,
    EMAIL         VARCHAR2(150) UNIQUE NOT NULL,
    PHONE         VARCHAR2(15)  NOT NULL,
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    ADDRESS       VARCHAR2(400),
    CREATED_AT    DATE          DEFAULT SYSDATE,
    IS_ACTIVE     NUMBER(1)     DEFAULT 1
);

-- ── CATEGORIES ───────────────────────────────────────────────
CREATE TABLE CATEGORIES (
    CATEGORY_ID   NUMBER        PRIMARY KEY,
    CATEGORY_NAME VARCHAR2(100) UNIQUE NOT NULL,
    DESCRIPTION   VARCHAR2(300)
);

-- ── PRODUCTS ─────────────────────────────────────────────────
CREATE TABLE PRODUCTS (
    PRODUCT_ID   NUMBER        PRIMARY KEY,
    PRODUCT_NAME VARCHAR2(200) NOT NULL,
    CATEGORY_ID  NUMBER        REFERENCES CATEGORIES(CATEGORY_ID),
    PRICE        NUMBER(10,2)  NOT NULL CHECK (PRICE >= 0),
    STOCK_QTY    NUMBER(10)    NOT NULL CHECK (STOCK_QTY >= 0),
    DESCRIPTION  VARCHAR2(500),
    IMAGE_URL    VARCHAR2(300),
    CREATED_AT   DATE          DEFAULT SYSDATE,
    IS_ACTIVE    NUMBER(1)     DEFAULT 1
);

-- ── CART ─────────────────────────────────────────────────────
CREATE TABLE CART (
    CART_ID     NUMBER       PRIMARY KEY,
    CUSTOMER_ID NUMBER       REFERENCES CUSTOMERS(CUSTOMER_ID),
    CREATED_AT  DATE         DEFAULT SYSDATE,
    STATUS      VARCHAR2(20) DEFAULT 'ACTIVE'
);

-- ── CART_ITEMS ───────────────────────────────────────────────
CREATE TABLE CART_ITEMS (
    CART_ITEM_ID NUMBER       PRIMARY KEY,
    CART_ID      NUMBER       REFERENCES CART(CART_ID),
    PRODUCT_ID   NUMBER       REFERENCES PRODUCTS(PRODUCT_ID),
    QUANTITY     NUMBER(10)   NOT NULL CHECK (QUANTITY > 0),
    UNIT_PRICE   NUMBER(10,2) NOT NULL
);

-- ── ORDERS ───────────────────────────────────────────────────
CREATE TABLE ORDERS (
    ORDER_ID         NUMBER        PRIMARY KEY,
    CUSTOMER_ID      NUMBER        REFERENCES CUSTOMERS(CUSTOMER_ID),
    ORDER_DATE       DATE          DEFAULT SYSDATE,
    TOTAL_AMOUNT     NUMBER(12,2)  NOT NULL,
    DELIVERY_ADDRESS VARCHAR2(400) NOT NULL,
    STATUS           VARCHAR2(30)  DEFAULT 'PLACED',
    PAYMENT_METHOD   VARCHAR2(50)  DEFAULT 'CASH ON DELIVERY'
);

-- ── ORDER_ITEMS ──────────────────────────────────────────────
-- NOTE: No VIRTUAL/GENERATED column — subtotal is computed in queries.
-- This prevents ORA insert errors caused by virtual column conflicts.
CREATE TABLE ORDER_ITEMS (
    ORDER_ITEM_ID NUMBER       PRIMARY KEY,
    ORDER_ID      NUMBER       REFERENCES ORDERS(ORDER_ID),
    PRODUCT_ID    NUMBER       REFERENCES PRODUCTS(PRODUCT_ID),
    QUANTITY      NUMBER(10)   NOT NULL CHECK (QUANTITY > 0),
    UNIT_PRICE    NUMBER(10,2) NOT NULL
);

-- ────────────────────────────────────────────────────────────
--  TRIGGERS
-- ────────────────────────────────────────────────────────────

-- Auto-decrement stock when an order item is inserted
CREATE OR REPLACE TRIGGER TRG_UPDATE_STOCK
AFTER INSERT ON ORDER_ITEMS
FOR EACH ROW
BEGIN
    UPDATE PRODUCTS
    SET    STOCK_QTY = STOCK_QTY - :NEW.QUANTITY
    WHERE  PRODUCT_ID = :NEW.PRODUCT_ID;
END;
/

-- Prevent order if stock is insufficient
CREATE OR REPLACE TRIGGER TRG_CHECK_STOCK
BEFORE INSERT ON ORDER_ITEMS
FOR EACH ROW
DECLARE
    v_stock NUMBER;
BEGIN
    SELECT STOCK_QTY INTO v_stock
    FROM   PRODUCTS
    WHERE  PRODUCT_ID = :NEW.PRODUCT_ID;

    IF v_stock < :NEW.QUANTITY THEN
        RAISE_APPLICATION_ERROR(-20001,
            'Insufficient stock for product ID ' || :NEW.PRODUCT_ID ||
            '. Available: ' || v_stock || ', Requested: ' || :NEW.QUANTITY);
    END IF;
END;
/

