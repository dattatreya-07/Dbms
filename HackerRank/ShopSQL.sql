CREATE OR REPLACE VIEW V_PRODUCT_DETAILS AS
SELECT p.PRODUCT_ID,
       p.PRODUCT_NAME,
       c.CATEGORY_NAME,
       p.PRICE,
       p.STOCK_QTY,
       p.DESCRIPTION,
       CASE WHEN p.IS_ACTIVE = 1 THEN 'Active' ELSE 'Inactive' END AS STATUS
FROM   PRODUCTS p
JOIN   CATEGORIES c ON p.CATEGORY_ID = c.CATEGORY_ID;

CREATE OR REPLACE VIEW V_ORDER_SUMMARY AS
SELECT o.ORDER_ID,
       cu.FULL_NAME,
       cu.EMAIL,
       TO_CHAR(o.ORDER_DATE, 'DD-Mon-YYYY HH24:MI') AS ORDER_DATE,
       o.TOTAL_AMOUNT,
       o.STATUS,
       o.DELIVERY_ADDRESS,
       o.PAYMENT_METHOD,
       (SELECT COUNT(*) FROM ORDER_ITEMS oi WHERE oi.ORDER_ID = o.ORDER_ID) AS ITEM_COUNT
FROM   ORDERS o
JOIN   CUSTOMERS cu ON o.CUSTOMER_ID = cu.CUSTOMER_ID;

CREATE OR REPLACE VIEW V_ORDER_ITEMS_DETAIL AS
SELECT o.ORDER_ID,
       cu.FULL_NAME,
       p.PRODUCT_NAME,
       c.CATEGORY_NAME,
       oi.QUANTITY,
       oi.UNIT_PRICE,
       (oi.QUANTITY * oi.UNIT_PRICE) AS SUBTOTAL,
       o.ORDER_DATE,
       o.STATUS
FROM   ORDER_ITEMS oi
JOIN   ORDERS   o  ON oi.ORDER_ID  = o.ORDER_ID
JOIN   PRODUCTS p  ON oi.PRODUCT_ID = p.PRODUCT_ID
JOIN   CATEGORIES c ON p.CATEGORY_ID = c.CATEGORY_ID
JOIN   CUSTOMERS cu ON o.CUSTOMER_ID = cu.CUSTOMER_ID;

